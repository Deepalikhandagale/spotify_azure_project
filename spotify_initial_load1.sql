DROP TABLE IF EXISTS DimUser;
DROP TABLE IF EXISTS DimArtist;
DROP TABLE IF EXISTS DimTrack;
DROP TABLE IF EXISTS DimDate;
DROP TABLE IF EXISTS FactStream;

-- DDL for Spotify Warehouse
CREATE TABLE DimUser (
  user_id INT PRIMARY KEY,
  user_name VARCHAR(255),
  country VARCHAR(255),
  subscription_type VARCHAR(50),
  start_date DATE,
  end_date DATE,
  updated_at DATETIME
);

CREATE TABLE DimArtist (
  artist_id INT PRIMARY KEY,
  artist_name VARCHAR(255),
  genre VARCHAR(100),
  country VARCHAR(100),
  updated_at DATETIME
);

CREATE TABLE DimTrack (
  track_id INT PRIMARY KEY,
  track_name VARCHAR(255),
  artist_id INT,
  album_name VARCHAR(255),
  duration_sec INT,
  release_date DATE,
  updated_at DATETIME
);

CREATE TABLE DimDate (
  date_key INT PRIMARY KEY,
  date DATE,
  day INT,
  month INT,
  year INT,
  weekday VARCHAR(20)
);

CREATE TABLE FactStream (
  stream_id BIGINT PRIMARY KEY,
  user_id INT,
  track_id INT,
  date_key INT,
  listen_duration INT,
  device_type VARCHAR(50),
  stream_timestamp DATETIME
);
